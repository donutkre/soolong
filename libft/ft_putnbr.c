#include "libft.h"

void	ft_putnbr(int n)
{
	ssize_t	r;
	char	c;

	if (n == -2147483648)
		r = write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			r = write(1, "-", 1);
		}
		if (n > 9)
			ft_putnbr(n / 10);
		c = n % 10 + '0';
		r = write(1, &c, 1);
	}
	(void)r;
}
