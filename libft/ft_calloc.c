#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	i;

	i = count * size;
	ptr = malloc(i);
	if (ptr)
		ft_memset(ptr, 0, i);
	return (ptr);
}
