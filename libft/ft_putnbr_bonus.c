#include "libft.h"

char	*ft_putnbr(long int n, char *s, int size)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		i++;
		n = -n;
	}
	s[size - 1] = '\0';
	while (--size > 0)
	{
		s[size - 1] = '0' + n % 10;
		n = n / 10;
	}
	if (i == 1)
		s[0] = '-';
	return (s);
}
